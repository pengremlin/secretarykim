package Project.DTO;

public class Lean {
	
	private int lean_no;
	private int project_no;
	private String project_name;
	private int lean_order;
	private String lean_content;
	
	
	
	public int getLean_no() {
		return lean_no;
	}
	public void setLean_no(int lean_no) {
		this.lean_no = lean_no;
	}
	public int getProject_no() {
		return project_no;
	}
	public void setProject_no(int project_no) {
		this.project_no = project_no;
	}
	public String getProject_name() {
		return project_name;
	}
	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}
	public int getLean_order() {
		return lean_order;
	}
	public void setLean_order(int lean_order) {
		this.lean_order = lean_order;
	}
	public String getLean_content() {
		return lean_content;
	}
	public void setLean_content(String lean_content) {
		this.lean_content = lean_content;
	}
	

}
